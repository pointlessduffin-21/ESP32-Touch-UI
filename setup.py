def generate_lvgl_image_c(img_w, img_h):
    """Convert vehicle_top.png to LVGL RGB565 + alpha C array."""
    try:
        from PIL import Image
    except ImportError:
        print("[!] Pillow not installed")
        sys.exit(1)

    png_path = ASSETS / "vehicle_top.png"
    img = Image.open(png_path).convert("RGBA")

    pixels = list(img.getdata())
    pixel_data = []
    for r, g, b, a in pixels:
        # LVGL v9 ARGB8888: B, G, R, A (little-endian layout)
        pixel_data.append(b)
        pixel_data.append(g)
        pixel_data.append(r)
        pixel_data.append(a)

    hex_rows = []
    for i in range(0, len(pixel_data), 16):
        chunk = pixel_data[i:i+16]
        hex_rows.append("    " + ", ".join(f"0x{b:02X}" for b in chunk) + ",")

    c_content = f"""\
/**
 * vehicle_top.c — LVGL image descriptor for top-down car silhouette
 * Generated by setup.py — do not edit manually
 * Format: LVGL v9 LV_COLOR_FORMAT_ARGB8888 (BGRA, 4 bytes/pixel)
 */

#ifdef __has_include
  #if __has_include("lvgl/lvgl.h")
    #include "lvgl/lvgl.h"
  #else
    #include "lvgl.h"
  #endif
#else
  #include "lvgl.h"
#endif

#ifndef LV_ATTRIBUTE_MEM_ALIGN
#define LV_ATTRIBUTE_MEM_ALIGN
#endif
#ifndef LV_ATTRIBUTE_IMG_VEHICLE_TOP
#define LV_ATTRIBUTE_IMG_VEHICLE_TOP
#endif

const LV_ATTRIBUTE_MEM_ALIGN LV_ATTRIBUTE_IMG_VEHICLE_TOP uint8_t vehicle_top_map[] = {{
{chr(10).join(hex_rows)}
}};

/* LVGL v9 image descriptor */
const lv_image_dsc_t vehicle_top = {{
    .header = {{
        .magic      = LV_IMAGE_HEADER_MAGIC,
        .cf         = LV_COLOR_FORMAT_ARGB8888,
        .w          = {img_w},
        .h          = {img_h},
        .stride     = {img_w * 4},
    }},
    .data_size  = {img_w * img_h * 4},
    .data       = vehicle_top_map,
}};
"""
    out = ASSETS / "vehicle_top.c"
    out.write_text(c_content)
    print(f"[2/6] LVGL image C array generated: {out}")


