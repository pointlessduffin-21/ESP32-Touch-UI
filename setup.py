def generate_vehicle_png():
    """Draw a top-down car silhouette using PIL and save as PNG."""
    try:
        from PIL import Image, ImageDraw, ImageFilter
    except ImportError:
        print("[!] Pillow not installed — run: pip3 install Pillow")
        sys.exit(1)

    IW, IH = 80, 160
    img = Image.new("RGBA", (IW, IH), (0, 0, 0, 0))
    d   = ImageDraw.Draw(img)

    # Body (rounded rect, silver gradient approximated with fill)
    body_poly = [
        (28, 8), (32, 6), (48, 6), (52, 8),
        (64, 14), (68, 30), (70, 50),
        (70, 115), (68, 130), (64, 148),
        (52, 155), (28, 155),
        (16, 148), (12, 130), (10, 115),
        (10, 50), (12, 30), (16, 14),
    ]
    d.polygon(body_poly, fill=(180, 180, 180, 255), outline=(80, 80, 80, 255))

    # Cabin roof area (darker)
    cabin_poly = [
        (22, 52), (58, 52), (58, 108), (22, 108)
    ]
    d.polygon(cabin_poly, fill=(140, 140, 140, 255), outline=(70, 70, 70, 200))

    # Roof highlight
    d.rectangle([28, 56, 52, 104], fill=(200, 200, 200, 180))

    # Front windshield (dark glass)
    wind_f = [(26, 32), (54, 32), (52, 52), (28, 52)]
    d.polygon(wind_f, fill=(30, 50, 70, 220))

    # Rear windshield
    wind_r = [(28, 108), (52, 108), (54, 128), (26, 128)]
    d.polygon(wind_r, fill=(30, 50, 70, 180))

    # Front headlights
    d.ellipse([13, 14, 26, 22], fill=(200, 220, 255, 230))
    d.ellipse([54, 14, 67, 22], fill=(200, 220, 255, 230))

    # Rear taillights
    d.ellipse([13, 138, 26, 148], fill=(255, 40, 40, 220))
    d.ellipse([54, 138, 67, 148], fill=(255, 40, 40, 220))

    # Side mirrors
    d.polygon([(10, 44), (6, 46), (6, 52), (10, 52)], fill=(140, 140, 140, 220))
    d.polygon([(70, 44), (74, 46), (74, 52), (70, 52)], fill=(140, 140, 140, 220))

    # Door crease line
    d.line([(10, 80), (70, 80)], fill=(100, 100, 100, 160), width=1)

    # Slight blur for smooth look
    img = img.filter(ImageFilter.SMOOTH)

    out = ASSETS / "vehicle_top.png"
    img.save(out, "PNG")
    print(f"[1/6] Vehicle PNG generated: {out}  ({IW}×{IH})")
    return IW, IH


